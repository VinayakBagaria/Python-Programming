import math
print(math.copysign(1,-2))    #Copy sign of y into x
print(math.fabs(-5.2))        #Absolute value
print(math.frexp(5))          #x=m * 2^e...show m and e
print(math.isinf(5))          #is float x +ve or -ve infinity
print(math.ldexp(2,3))        #return x * 2^i
print(math.modf(-5.3))        #return fractional and integral parts ; if -ve sign on both
print(math.exp(5))            #e^x
print(math.expm1(5))          #e^x - 1
print(math.log(100,10))       #log of x with base y.. if no y,then default = e
print(math.cos(60))
print(math.hypot(4,3))        #print sqrt of (x^2 + y^2)
print(math.e)
print(math.pi)